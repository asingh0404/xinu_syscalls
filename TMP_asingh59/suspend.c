/* suspend.c - suspend */

#include <conf.h>
#include <kernel.h>
#include <proc.h>
#include <q.h>
#include <stdio.h>
#include <lab0.h>

/*------------------------------------------------------------------------
 *  suspend  --  suspend a process, placing it in hibernation
 *------------------------------------------------------------------------
 */
SYSCALL	suspend(int pid)
{

	int funcId = 24;

	unsigned long startTime, endTime;

	int startTrackFlag = 0;

	if(trackFlag==1)
	{
			sysList[currpid][funcId].sysCallName = __func__;
			sysList[currpid][funcId].pid = currpid;
			sysList[currpid][funcId].freq += 1;
			startTime = ctr1000;
			startTrackFlag = 1;
	}

	STATWORD ps;    
	struct	pentry	*pptr;		/* pointer to proc. tab. entry	*/
	int	prio;			/* priority returned		*/

	disable(ps);
	if (isbadpid(pid) || pid==NULLPROC ||
	 ((pptr= &proctab[pid])->pstate!=PRCURR && pptr->pstate!=PRREADY)) {
		restore(ps);

	    if(trackFlag == 1 && startTrackFlag == 1)
    	{
        	unsigned long timeElapsed = ctr1000 - startTime;
        	sysList[currpid][funcId].execTime += timeElapsed;
    	}	

		return(SYSERR);
	}
	if (pptr->pstate == PRREADY) {
		pptr->pstate = PRSUSP;
		dequeue(pid);
	}
	else {
		pptr->pstate = PRSUSP;
		resched();
	}
	prio = pptr->pprio;
	restore(ps);

    if(trackFlag == 1 && startTrackFlag == 1)
    {
        unsigned long timeElapsed = ctr1000 - startTime;
        sysList[currpid][funcId].execTime += timeElapsed;
    }

	return(prio);
}
